# DDL — CRIAÇÃO DAS TABELAS
# ─────────────────────────────────────────────────────────────

DDL_VISITANTES = """
CREATE TABLE visitantes (
    id          NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    nome        VARCHAR2(100),
    perfil      VARCHAR2(50),       -- estudante, turista, pesquisador, profissional
    idade_faixa VARCHAR2(20),       -- jovem, adulto, idoso
    criado_em   TIMESTAMP DEFAULT CURRENT_TIMESTAMP
)
"""

DDL_INTERACOES = """
CREATE TABLE interacoes (
    id              NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    visitante_id    NUMBER REFERENCES visitantes(id),
    tipo_acao       VARCHAR2(50),   -- chatbot, imagem, menu, busca
    categoria       VARCHAR2(50),   -- arte, ciencia, historia, tecnologia
    duracao_seg     NUMBER(5),      -- tempo de interação em segundos
    satisfacao      NUMBER(1),      -- avaliação 1-5
    registrado_em   TIMESTAMP DEFAULT CURRENT_TIMESTAMP
)
"""

DDL_LOGS_SISTEMA = """
CREATE TABLE logs_sistema (
    id          NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    modulo      VARCHAR2(50),       -- vision, chatbot, classificador
    evento      VARCHAR2(200),
    nivel       VARCHAR2(10),       -- INFO, WARN, ERROR
    registrado_em TIMESTAMP DEFAULT CURRENT_TIMESTAMP
)
"""


def criar_tabelas():
    """
    Cria todas as tabelas necessárias no banco Oracle.
    Ignora erro ORA-00955 (tabela já existe).
    """
    tabelas = {
        "visitantes": DDL_VISITANTES,
        "interacoes": DDL_INTERACOES,
        "logs_sistema": DDL_LOGS_SISTEMA,
    }

    conn = get_connection()
    cursor = conn.cursor()

    for nome, ddl in tabelas.items():
        try:
            cursor.execute(ddl)
            print(f"[DB] Tabela '{nome}' criada com sucesso.")
        except oracledb.DatabaseError as e:
            error, = e.args
            if error.code == 955:  # ORA-00955: name is already used
                print(f"[DB] Tabela '{nome}' já existe, ignorando.")
            else:
                raise

    conn.commit()
    cursor.close()
    conn.close()


