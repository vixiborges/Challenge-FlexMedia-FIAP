def inserir_interacao(visitante_id: int, tipo_acao: str,
                       categoria: str, duracao_seg: int,
                       satisfacao: int) -> int:
    """Registra uma interação do visitante com o tótem."""
    sql = """
        INSERT INTO interacoes (visitante_id, tipo_acao, categoria, duracao_seg, satisfacao)
        VALUES (:visitante_id, :tipo_acao, :categoria, :duracao_seg, :satisfacao)
        RETURNING id INTO :novo_id
    """
    conn = get_connection()
    cursor = conn.cursor()

    novo_id = cursor.var(oracledb.NUMBER)
    cursor.execute(sql, {
        "visitante_id": visitante_id,
        "tipo_acao": tipo_acao,
        "categoria": categoria,
        "duracao_seg": duracao_seg,
        "satisfacao": satisfacao,
        "novo_id": novo_id
    })
    conn.commit()
    cursor.close()
    conn.close()

    return int(novo_id.getvalue()[0])


def listar_interacoes(limit: int = 500) -> list[dict]:
    """Retorna as últimas N interações registradas."""
    sql = """
        SELECT i.id, v.nome, v.perfil, i.tipo_acao, i.categoria,
               i.duracao_seg, i.satisfacao, i.registrado_em
        FROM interacoes i
        JOIN visitantes v ON v.id = i.visitante_id
        ORDER BY i.registrado_em DESC
        FETCH FIRST :limit ROWS ONLY
    """
    conn = get_connection()
    cursor = conn.cursor()
    cursor.execute(sql, {"limit": limit})
    colunas = [col[0].lower() for col in cursor.description]
    rows = [dict(zip(colunas, row)) for row in cursor.fetchall()]
    cursor.close()
    conn.close()
    return rows


def contar_interacoes_por_categoria() -> list[dict]:
    """Agrupa interações por categoria — usado nos gráficos de analytics."""
    sql = """
        SELECT categoria, COUNT(*) AS total
        FROM interacoes
        GROUP BY categoria
        ORDER BY total DESC
    """
    conn = get_connection()
    cursor = conn.cursor()
    cursor.execute(sql)
    rows = [{"categoria": r[0], "total": r[1]} for r in cursor.fetchall()]
    cursor.close()
    conn.close()
    return rows


def media_satisfacao_por_tipo() -> list[dict]:
    """Retorna a média de satisfação por tipo de ação."""
    sql = """
        SELECT tipo_acao, ROUND(AVG(satisfacao), 2) AS media_satisfacao
        FROM interacoes
        GROUP BY tipo_acao
        ORDER BY media_satisfacao DESC
    """
    conn = get_connection()
    cursor = conn.cursor()
    cursor.execute(sql)
    rows = [{"tipo_acao": r[0], "media_satisfacao": r[1]} for r in cursor.fetchall()]
    cursor.close()
    conn.close()
    return rows


