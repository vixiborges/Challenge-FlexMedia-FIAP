def inserir_visitante(nome: str, perfil: str, idade_faixa: str) -> int:
    """Insere um novo visitante e retorna o ID gerado."""
    sql = """
        INSERT INTO visitantes (nome, perfil, idade_faixa)
        VALUES (:nome, :perfil, :idade_faixa)
        RETURNING id INTO :novo_id
    """
    conn = get_connection()
    cursor = conn.cursor()

    novo_id = cursor.var(oracledb.NUMBER)
    cursor.execute(sql, {"nome": nome, "perfil": perfil,
                         "idade_faixa": idade_faixa, "novo_id": novo_id})
    conn.commit()
    cursor.close()
    conn.close()

    return int(novo_id.getvalue()[0])


def listar_visitantes(limit: int = 100) -> list[dict]:
    """Retorna os últimos N visitantes registrados."""
    sql = """
        SELECT id, nome, perfil, idade_faixa, criado_em
        FROM visitantes
        ORDER BY criado_em DESC
        FETCH FIRST :limit ROWS ONLY
    """
    conn = get_connection()
    cursor = conn.cursor()
    cursor.execute(sql, {"limit": limit})
    colunas = [col[0].lower() for col in cursor.description]
    rows = [dict(zip(colunas, row)) for row in cursor.fetchall()]
    cursor.close()
    conn.close()
    return rows


